// Requries 'Bearer TOKENxxxxx'
var jwtVerify = require('../../reuse/jwtVerify');

// Requires < SQL > , < ARRAY_PARAMETER >
var query = require('../../reuse/query')

var sql = require('../../../mysql/queries/accounts/Login')

const setUserState = async ({res, token, params}) => {
    
    let error  = false;    
    let jwtResult;
    let sqlResult;

    try{
        jwtResult= await jwtVerify(token);
    } catch(err) {
        error  = true; 
    }
    
    if(!jwtResult) {
        error  = true; 
    }

    try{       
        sqlResult = await query(sql.select_blacklist_token, [jwtResult.token, jwtResult.decoded.id])
    } catch (err) {
        error  = true; 
    }
    
    // set acadYear
    try{
        if(sqlResult[0].is_token === 'AUTH') {

            let sql = `UPDATE account  as main
                        SET main.state = (
                            SELECT * FROM (SELECT IF(state= 1 , '0', '1') as DATA FROM account WHERE id = ? LIMIT 1) as sub
                        )
                        WHERE main.id =?`;

            sqlResult = await query(sql, params[0])
        }
    } catch (err){
        error  = true;  
    }

    error ? 
        res.sendStatus(401) : 
        res.json({ data:sqlResult})
}

module.exports =  setUserState